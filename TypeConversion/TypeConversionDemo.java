
class TypeConversionDemo {

    public static void main(String args[]) {

        //Type conversion and Casting
        byte b = 127;
        int a = b;

        float f = 5.5f;
        int t = (int) f;

        byte x = 10;
        byte y = 40;

        int result = x * y;

        System.out.println(b);
        System.out.println(a);
        System.out.println(t);

        System.out.println(result);
    }
}
